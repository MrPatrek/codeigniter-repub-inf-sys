	<h2><?= $title ;?></h2>
	<p>RePub is a website which allows citizens to report public nuisance.</p>
	<p>It is actually an implementation of an information system for <strong>re</strong>porting <strong>pub</strong>lic nuisance.</p>
	<hr>
	<p>There are three different roles for using this website: <strong>Citizen</strong>, <strong>Authorities</strong> and <strong>Moderator</strong>.</p>
	<br>
	<p><strong>Citizen</strong>: can create reports, can appeal on authoritie's decisions, can send edit or delete requests for some report/appeal</p>
	<p><strong>Authorities</strong>: can asnwer citizens' reports, can approve/disapprove delete requests.</p>
	<p><strong>Moderator</strong>: can approve/disapprove edit requests.</p>
	<br>
	<p>If you are not logged in (which also means you have no role), you can still see reports and appeals to them as well as profiles of these users.</p>
